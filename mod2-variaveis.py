# 2.1 - Variaveis

# Quais tipos de dados podemos armazenar na memória?

# Números
velocidade_internet = 10
print(velocidade_internet)
print(type(velocidade_internet))

# Boleanos
porta_aberta = True # or False
print(porta_aberta)
print(type(porta_aberta))

# Strings
slogan = 'Feito é melhor que perfeito!'
print(slogan)
print(type(slogan))

# Variaveis em cadeia
a,b,c,d = 1,2,3,4
print(a,b,c,d)
print(type(a))

# DESAFIO
variavel_1 = 25.87
variavel_2 = True
variavel_3 = 'Bom dia!'
variavel_4 = 15

print('DESAFIO')
print(type(variavel_1))
print(type(variavel_2))
print(type(variavel_3))
print(type(variavel_4))
